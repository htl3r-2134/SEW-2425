public class Vehicle {

    protected int seats;
    protected int capacity;
    protected boolean hasTailLift;


    public int getSeats() {
        return seats;
    }

    public int getCapacity() {
        return capacity;
    }
    public boolean hasTailLift() {
        return hasTailLift;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
